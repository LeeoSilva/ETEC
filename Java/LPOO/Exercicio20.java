public class Exercicio20{
  public static void main(String args []){
    //Crie uma class que mostre na tela todas as letras o alfabeto usando o comando FOR.
    char alfabet = 'A';
    System.out.print("A ");
    for(int i = 0; i <= 24; i++){ 
      alfabet++;
      System.out.print(alfabet + " ");
    }
    System.out.print("\n");
  }
}
